INSERT IGNORE INTO inventory_items (product_id, quantity_on_hand, reorder_threshold)
SELECT id, FLOOR(50 + RAND() * 200), FLOOR(10 + RAND() * 30)
FROM products
WHERE id NOT IN (SELECT product_id FROM inventory_items);

-- ========================
-- Replenishment Alerts
-- ========================
INSERT IGNORE INTO replenishment_alerts (inventory_item_id, alerted_at, resolved)
SELECT inv.id, CURRENT_TIMESTAMP, 0
FROM inventory_items inv
JOIN products p ON p.id = inv.product_id
WHERE inv.quantity_on_hand < inv.reorder_threshold
AND NOT EXISTS (
    SELECT 1 FROM replenishment_alerts ra WHERE ra.inventory_item_id = inv.id AND ra.resolved = 0
);